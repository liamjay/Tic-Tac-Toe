package TicTacToe;

public class Player {
    private String name;
    private char marker;

    public String getName()
    {
        return this.name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public char getMarker()
    {
        return this.marker;
    }

    public void setMarker(char marker)
    {
        this.marker = marker;
    }
}
