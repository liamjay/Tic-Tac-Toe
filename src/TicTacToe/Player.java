package TicTacToe;

public class Player {
    private String name;
    private String marker;

    public String getName()
    {
        return this.name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public String getMarker()
    {
        return this.marker;
    }

    public void setMarker(String marker)
    {
        this.marker = marker;
    }
}
